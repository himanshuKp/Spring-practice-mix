package Basic;

public class VarPractice {
    public static void main(String[] args) {
        var avg = 10.0;
        System.out.println("Value  of avg: "+avg);  //valid

        int var = 2;
        System.out.println("Value of var: "+var);   //valid

        var k = --var;
        System.out.println("Value of k: "+k);   //valid
    }
}
